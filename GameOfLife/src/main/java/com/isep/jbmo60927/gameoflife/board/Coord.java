package com.isep.jbmo60927.gameoflife.board;

public class Coord {
    private final int x;
    private final int y;

    public Coord(int x, int y){
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
